    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            if (!txtNomor.getText().isEmpty() && ijinTrayek != null & armada != null) {
                if (selected == null) {
                    KartuPengawasanOtobisUmum k = new KartuPengawasanOtobisUmum();
                    k.setNomor(txtNomor.getText());
                    k.setTanggal(tglKartu.getDate());
                    k.setIjinTrayek(ijinTrayek);
                    k.setArmada(armada);
                    k.setTanggalBerlakuMulai(tglBerlakuMulai.getDate());
                    k.setTanggalBerlakuSelesai(tglBerlakuSelesai.getDate());
                    dao.insert(k);
                    armada = null;
                    ijinTrayek = null;
                } else {
                    selected.setNomor(txtNomor.getText());
                    selected.setTanggal(tglKartu.getDate());
                    selected.setIjinTrayek(ijinTrayek);
                    selected.setArmada(armada);
                    selected.setTanggalBerlakuMulai(tglBerlakuMulai.getDate());
                    selected.setTanggalBerlakuSelesai(tglBerlakuSelesai.getDate());
                    dao.update(selected);
                    selected = null;
                    armada = null;
                    ijinTrayek = null;
                }
                reloadAllKartu();
                txtNomor.setText("");
                tglKartu.setDate(new Date());
                txtIjinTrayek.setText("");
                txtKendaraanArmada.setText("");
                tglBerlakuMulai.setDate(new Date());
                tglBerlakuSelesai.setDate(new Date());
                JOptionPane.showMessageDialog(this, "Data tersimpan");
                tabpane.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(this, "Input data dengan lengkap");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

