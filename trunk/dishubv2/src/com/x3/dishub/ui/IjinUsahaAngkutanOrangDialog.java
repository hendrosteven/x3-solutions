    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            if (!txtNomor.getText().isEmpty() && !txtNomorPermohonan.getText().isEmpty()) {
                if (selected == null) {
                    IjinUsahaAngkutanOrang i = new IjinUsahaAngkutanOrang();
                    i.setNomor(txtNomor.getText());
                    i.setTanggal(tglSurat.getDate());
                    i.setPerusahaan((Perusahaan) cmbPerusahaan.getSelectedItem());
                    i.setTglSuratPermohonan(tglPermohonan.getDate());
                    i.setNomorSuratPermohonan(txtNomorPermohonan.getText());
                    dao.insert(i);
                } else {
                    selected.setNomor(txtNomor.getText());
                    selected.setTanggal(tglSurat.getDate());
                    selected.setPerusahaan((Perusahaan) cmbPerusahaan.getSelectedItem());
                    selected.setTglSuratPermohonan(tglPermohonan.getDate());
                    selected.setNomorSuratPermohonan(txtNomorPermohonan.getText());
                    dao.update(selected);
                    selected = null;
                }
                loadIjin();
                txtNomor.setText("");
                tglSurat.setDate(new Date());
                cmbPerusahaan.setSelectedItem(null);
                txtNomorPermohonan.setText("");
                tglPermohonan.setDate(new Date());
                JOptionPane.showMessageDialog(this, "Data tersimpan");
            } else {
                JOptionPane.showMessageDialog(this, "Masukan data dengan lengkap");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        try {
            if (selected != null) {
                //ambil parameter
                Map mp = new HashMap();
                mp.put("nomor", selected.getNomor());

                DataSource ds = (DataSource) MainApps.appContext.getBean("dataSource");
                JDialog printView = new JDialog();
                JasperPrint print = JasperFillManager.fillReport("print/ijinUsahaUmum.jasper",
                        mp, ds.getConnection());
                JRViewer viewer = new JRViewer(print);
                printView.getContentPane().add(viewer);
                printView.setModal(false);
                printView.setTitle("Print Ijin Usaha");
                printView.setSize(800, 500);
                printView.setVisible(true);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnCetakActionPerformed

