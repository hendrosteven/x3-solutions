    private void loadWarna() {
        headerColor = wDao.getByNama("IjinUsahaAngkutanOrangDialogHd");
        if (headerColor == null) {
            headerColor = new Warna();
            headerColor.setName("IjinUsahaAngkutanOrangDialogHd");
            headerColor.setRgb(-16777012);
            wDao.insert(headerColor);
            loadWarna();
        }
        backgroudColor = wDao.getByNama("IjinUsahaAngkutanOrangDialogBg");
        if (backgroudColor == null) {
            backgroudColor = new Warna();
            backgroudColor.setName("IjinUsahaAngkutanOrangDialogBg");
            backgroudColor.setRgb(-13408513);
            wDao.insert(backgroudColor);
            loadWarna();
        }
    }

    private void updateWarna() {
        headerPanel.setBackground(new Color(headerColor.getRgb()));
        this.getContentPane().setBackground(new Color(backgroudColor.getRgb()));
    }

    private void loadIjin() {
        try {
            list = dao.getAllIjinUsahaAngkutanOrang();
            String title[] = {"Nomor", "Tanggal", "Perusahaan", "Pimpinan"};
            Object data[][] = new Object[list.size()][4];
            int row = 0;
            for (IjinUsahaAngkutanOrang i : list) {
                data[row][0] = i.getNomor();
                data[row][1] = formater.format(i.getTanggal());
                data[row][2] = i.getPerusahaan().getNama();
                data[row][3] = i.getPerusahaan().getNamaPimpinan();
                ++row;
            }
            TableModel model = new DefaultTableModel(data, title);
            tblIjin.setModel(model);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

