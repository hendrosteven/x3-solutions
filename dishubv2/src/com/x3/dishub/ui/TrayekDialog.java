    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            if (!txtKode.getText().isEmpty() && !txtNama.getText().isEmpty() && !txtDasarHukum.getText().isEmpty() && !txtKeterangan.getText().isEmpty()) {
                if (selected == null) {
                    Trayek t = new Trayek();
                    t.setKode(txtKode.getText());
                    t.setNama(txtNama.getText());
                    t.setDasarHukum(txtDasarHukum.getText());
                    t.setKeterangan(txtKeterangan.getText());
                    dao.insert(t);
                } else {
                    selected.setKode(txtKode.getText());
                    selected.setNama(txtNama.getText());
                    selected.setDasarHukum(txtDasarHukum.getText());
                    selected.setKeterangan(txtKeterangan.getText());
                    dao.update(selected);
                    selected = null;
                }
                txtKode.setText("");
                txtNama.setText("");
                txtDasarHukum.setText("");
                txtKeterangan.setText("");
                lblImage.setIcon(null);
                loadTable();
                JOptionPane.showMessageDialog(this, "Data tersimpan");
            } else {
                JOptionPane.showMessageDialog(this, "Input data dengan lengkap");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

