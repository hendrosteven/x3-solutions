    private byte[] readImage(File file) throws IOException {
        //Logger.getLogger(Main.class.getName()).log(Level.INFO, "[Open File] " + file.getAbsolutePath());
        InputStream is = new FileInputStream(file);
        // Get the size of the file
        long length = file.length();
        // You cannot create an array using a long type.
        // It needs to be an int type.
        // Before converting to an int type, check
        // to ensure that file is not larger than Integer.MAX_VALUE.
        if (length > Integer.MAX_VALUE) {
            // File is too large
        }
        // Create the byte array to hold the data
        byte[] bytes = new byte[(int) length];
        // Read in the bytes
        int offset = 0;
        int numRead = 0;
        while (offset < bytes.length && (numRead = is.read(bytes, offset, bytes.length - offset)) >= 0) {
            offset += numRead;
        }
        // Ensure all the bytes have been read in
        if (offset < bytes.length) {
            throw new IOException("Could not completely read file " + file.getName());
        }
        // Close the input stream and return bytes
        is.close();
        return bytes;
    }

    private void btnOpenFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenFileActionPerformed
        if (selected != null) {
            JFileChooser chooser = new JFileChooser("C:/");
            chooser.setFileFilter(new FileFilter() {
                //Accept all directories and all gif, jpg, tiff, or png files.

                public boolean accept(File f) {
                    if (f.isDirectory()) {
                        return true;
                    }
                    String extension = Utils.getExtension(f);
                    if (extension != null) {
                        if (extension.equals(Utils.tiff)
                                || extension.equals(Utils.tif)
                                || extension.equals(Utils.gif)
                                || extension.equals(Utils.jpeg)
                                || extension.equals(Utils.jpg)
                                || extension.equals(Utils.png)) {
                            return true;
                        } else {
                            return false;
                        }
                    }
                    return false;
                }
                //The description of this filter

                public String getDescription() {
                    return "Just Images";
                }
            });
            chooser.setAcceptAllFileFilterUsed(false);

            int returnInterval = chooser.showDialog(this, "Pilih");
            if (returnInterval == JFileChooser.APPROVE_OPTION) {
                File file = chooser.getSelectedFile();
                ImageIcon img = new ImageIcon(file.getAbsolutePath());
                lblImage.setIcon(img);
                try {
                    selected.setPeta(readImage(file));
                    dao.update(selected);
                    JOptionPane.showMessageDialog(this, "Gambar Peta tersimpan");
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
            chooser.setSelectedFile(null);
        } else {
            JOptionPane.showMessageDialog(this, "Silahkan pilih trayek terlebih dahulu");
        }
    }//GEN-LAST:event_btnOpenFileActionPerformed

