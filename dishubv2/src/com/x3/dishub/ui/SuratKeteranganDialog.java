    private void btnCariNomorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariNomorActionPerformed
        try {
            String nomor = JOptionPane.showInputDialog(this, "Nomor :");
            list = dao.getByNomor(nomor);
            String title[] = {"Nomor", "Tanggal", "No.Kendaraan", "Pemilik Baru"};
            Object data[][] = new Object[list.size()][4];
            int row = 0;
            for (SuratKeterangan s : list) {
                data[row][0] = s.getNomor();
                data[row][1] = formater.format(s.getTanggal());
                data[row][2] = s.getKendaraan().getNomorPolisi();
                data[row][3] = s.getKendaraan().getPemilik().get(s.getKendaraan().getPemilik().size() - 1).getNamaDepan() + " "
                        + s.getKendaraan().getPemilik().get(s.getKendaraan().getPemilik().size() - 1).getNamaBelakang();
                ++row;
            }
            TableModel model = new DefaultTableModel(data, title);
            tblSurat.setModel(model);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnCariNomorActionPerformed

