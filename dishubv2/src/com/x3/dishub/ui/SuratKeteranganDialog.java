    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            if (selected == null) {
                SuratKeterangan s = new SuratKeterangan();
                s.setNomor(txtNomor.getText());
                s.setTanggal(tglSurat.getDate());
                s.setKendaraan(selectedKendaraan);
                s.setPemilikBaru(selectedKendaraan.getPemilik().get(selectedKendaraan.getPemilik().size() - 1));
                s.setPemilik(selectedKendaraan.getPemilik().get(selectedKendaraan.getPemilik().size() - 2));
                s.setLainLain(txtLainLain.getText());
                dao.insert(s);
                selectedKendaraan = null;
            } else {
                selected.setNomor(txtNomor.getText());
                selected.setTanggal(tglSurat.getDate());
                selected.setKendaraan(selectedKendaraan);
                selected.setPemilikBaru(selectedKendaraan.getPemilik().get(selectedKendaraan.getPemilik().size() - 1));
                selected.setPemilik(selectedKendaraan.getPemilik().get(selectedKendaraan.getPemilik().size() - 2));
                selected.setLainLain(txtLainLain.getText());
                dao.update(selected);
                selected = null;
            }
            loadSurat();
            txtNomor.setText("");
            tglSurat.setDate(new Date());
            txtKendaraan.setText("");
            txtNoUji.setText("");
            txtMerkTypeJenis.setText("");
            txtNoRangka.setText("");
            txtNoMesin.setText("");
            txtTahun.setText("");
            txtPemilik.setText("");
            txtAlamatPemilik.setText("");
            txtPemilikBaru.setText("");
            txtAlamatPemilikBaru.setText("");
            txtLainLain.setText("");
            JOptionPane.showMessageDialog(this, "Data tersimpan");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

