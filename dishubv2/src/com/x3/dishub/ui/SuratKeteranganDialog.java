    private void loadWarna() {
        headerColor = wDao.getByNama("SuratKeteranganDialogHd");
        if (headerColor == null) {
            headerColor = new Warna();
            headerColor.setName("SuratKeteranganDialogHd");
            headerColor.setRgb(-16777012);
            wDao.insert(headerColor);
            loadWarna();
        }
        backgroudColor = wDao.getByNama("SuratKeteranganDialogBg");
        if (backgroudColor == null) {
            backgroudColor = new Warna();
            backgroudColor.setName("SuratKeteranganDialogBg");
            backgroudColor.setRgb(-13408513);
            wDao.insert(backgroudColor);
            loadWarna();
        }
    }

    private void updateWarna() {
        headerPanel.setBackground(new Color(headerColor.getRgb()));
        this.getContentPane().setBackground(new Color(backgroudColor.getRgb()));
    }

    private void loadSurat() {
        try {
            list = dao.getAllSuratKeterangan();
            String title[] = {"Nomor", "Tanggal", "No.Kendaraan", "Pemilik Baru"};
            Object data[][] = new Object[list.size()][4];
            int row = 0;
            for (SuratKeterangan s : list) {
                data[row][0] = s.getNomor();
                data[row][1] = formater.format(s.getTanggal());
                data[row][2] = s.getKendaraan().getNomorPolisi();
                data[row][3] = s.getKendaraan().getPemilik().get(s.getKendaraan().getPemilik().size() - 1).getNamaDepan() + " "
                        + s.getKendaraan().getPemilik().get(s.getKendaraan().getPemilik().size() - 1).getNamaBelakang();
                ++row;
            }
            TableModel model = new DefaultTableModel(data, title);
            tblSurat.setModel(model);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

